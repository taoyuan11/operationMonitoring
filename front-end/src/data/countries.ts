const countryOptionsByCode = new Map<string, CountryOption>(
  COUNTRY_OPTIONS.map((country) => [country.code, country]),
)

export function getCountryOption(countryCode?: string) {
  return countryOptionsByCode.get((countryCode || '').trim().toUpperCase())
}

export function getCountryFlagUrl(countryCode?: string) {
  const country = getCountryOption(countryCode)
  if (!country) return undefined

  return flagAssets[`${flagAssetPrefix}${country.flag}.svg`]
}
